using System;
//test class for github
public class TestClass
{
    public TestClass() { }

    /// <summary>
    /// adds a with b
    /// </summary>
    /// <param name="a"></param>
    /// <param name="b"></param>
    /// <returns></returns>
    private int Add(int a, int b)
    {
        return a + b;
    }

    /// <summary>
    /// subtract b from a
    /// </summary>
    /// <param name="a"></param>
    /// <param name="b"></param>
    /// <returns></returns>
    private int Subtract(int a, int b)
    {
        return a - b;
    }
}
